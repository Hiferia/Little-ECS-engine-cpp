Map::Map()
{
	dirt = TextureMgr::LoadTexture("Assets/dirt.png");
	grass = TextureMgr::LoadTexture("Assets/grass.png");
	water = TextureMgr::LoadTexture("Assets/water.png");

	LoadMap(level1);

	sourceRect.x = 0;
	sourceRect.y = 0;
	sourceRect.w = 32;
	sourceRect.h = 32;
	destRect.w = 32;
	destRect.h = 32;

	destRect.x = 0;
	destRect.y = 0;
}

Map::~Map()
{
	SDL_DestroyTexture(grass);
	SDL_DestroyTexture(water);
	SDL_DestroyTexture(dirt);
}

void Map::LoadMap(int arr[20][25])
{
	for (int row = 0; row < 20; row++)
	{
		for (int col = 0; col < 25; col++)
		{
			map[row][col] = arr[row][col];
		}
	}
}

void Map::DrawMap()
{
	int type = 0;

	for (int row = 0; row < 20; row++)
	{
		for (int col = 0; col < 25; col++)
		{
			type = map[row][col];

			destRect.x = col * 32;
			destRect.y = row * 32;

			switch (type)
			{
			case 0:
				TextureMgr::Draw(water, sourceRect, destRect);
				break;
			case 1:
				TextureMgr::Draw(grass, sourceRect, destRect);
				break;
			case 2:
				TextureMgr::Draw(dirt, sourceRect, destRect);
				break;
			default:
				break;
			}
		}
	}
}
